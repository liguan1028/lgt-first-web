# 根据年龄展示不同结果

print("===== 年龄分类程序 =====")
print("")

while True:
    输入 = input("请输入您的年龄（输入 q 退出）: ").strip()

    if 输入.lower() == "q":
        print("再见！")
        break

    if not 输入.isdigit():
        print("  请输入有效的数字年龄。\n")
        continue

    年龄 = int(输入)

    if 年龄 < 0:
        print("  年龄不能为负数，请重新输入。\n")
        continue

    if 年龄 <= 2:
        阶段 = "婴幼儿"
        描述 = "正处于快速成长的阶段，需要家人悉心照料。"
    elif 年龄 <= 12:
        阶段 = "儿童"
        描述 = "可以尽情玩耍、学习基础知识，培养兴趣爱好。"
    elif 年龄 <= 17:
        阶段 = "青少年"
        描述 = "正在经历学业与自我认知的重要时期，加油！"
    elif 年龄 <= 35:
        阶段 = "青年"
        描述 = "正是探索事业、建立人际关系的好时光。"
    elif 年龄 <= 59:
        阶段 = "中年"
        描述 = "事业与家庭并重，经验与智慧日益丰富。"
    else:
        阶段 = "老年"
        描述 = "可以享受生活、分享人生阅历，保持健康最重要。"

    print("")
    print("  您的年龄: " + str(年龄) + " 岁")
    print("  所属阶段: " + 阶段)
    print("  温馨提示: " + 描述)

    if 年龄 < 18:
        print("  特殊提示: 您还未成年，许多活动需要监护人陪同。")
    elif 年龄 >= 60:
        print("  特殊提示: 您可能符合部分公共交通、景区的优惠政策。")
    else:
        print("  特殊提示: 您已具备完全民事行为能力。")

    print("")
